CREATE PROCEDURE SP_INSERT_DEVOLUTION_DATA
(
	@COD_CLIENTE INT,
	@COD_FITA INT
)
AS
BEGIN
	UPDATE LOCACAO
	SET DATA_DEVOLUCAO = GETDATE()
	WHERE
		ID_CLIENTE = @COD_CLIENTE AND ID_FITA = @COD_FITA
END
GO

CREATE PROCEDURE SP_SEARCH_MOVIE
(
	@MOVIE_NAME VARCHAR(60)
)
AS
BEGIN
	SELECT
		DESCRICAO AS MOVIE_NAME
	FROM
		FILME
	WHERE
		DESCRICAO LIKE '%' + @MOVIE_NAME +'%'
END
